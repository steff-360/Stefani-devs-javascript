// Suma
let puntosTotales = partido1 + partido2 + partido3;

// División (promedio)
let promedio = puntosTotales / 3;

// Resta (diferencia entre el mejor y peor partido)
let diferencia = partido2 - partido3;

// Multiplicación (porcentaje de rendimiento)
let porcentaje = (puntosTotales * 100) / 90;

// Módulo
let residuo = puntosTotales % 2;

// Mostrar resultados
